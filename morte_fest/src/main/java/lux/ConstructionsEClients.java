package lux;

public class ConstructionsEClients {
	private String company;
	private String construction;
	private String locationOfconstruction;
	private String priority;
	
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getConstruction() {
		return construction;
	}
	public void setConstruction(String construction) {
		this.construction = construction;
	}
	public String getLocationOfconstruction() {
		return locationOfconstruction;
	}
	public void setLocationOfconstruction(String locationOfconstruction) {
		this.locationOfconstruction = locationOfconstruction;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	
}
